        <h3>3.Seguridad de la información</h3>
        <p>Implementamos medidas de seguridad internas para proteger la información personal contra accesos no autorizados, alteraciones y divulgación. Esto incluye revisiones internas y barreras físicas de seguridad en nuestros servidores y sistemas.</p>
        <p>Solo el personal autorizado de Clap tiene acceso a información personal, y estos empleados están obligados a respetar acuerdos de confidencialidad. En caso de incumplimiento, podrían estar sujetos a sanciones.</p>

        <h3>4.Uso de la Información</h3>
        <p>La información personal recopilada en Clap se utiliza para los siguientes fines:</p>
        <ul>
            <li>Facilitar la comunicación con el usuario.</li>
            <li>Optimizar la experiencia de uso y reducir la necesidad de ingresar datos repetidamente.</li>
            <li>Personalizar los servicios y la publicidad mostrada en la plataforma.</li>
            <li>Realizar revisiones y análisis que permitan mejorar y proteger los servicios.</li>
            <li>Asegurar el funcionamiento técnico de Clap y proteger los derechos tanto de CodeSailing como de nuestros usuarios.</li>
            <li>Desarrollar nuevos servicios y funcionalidades que respondan a las necesidades de los usuarios.</li>
        </ul>

